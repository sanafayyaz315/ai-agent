# actions
def add(input: dict):
    """
    Tool: add
    Description: Takes two integers or floats and returns their sum
    """
    x = input["x"]
    y = input["y"]
    return x + y

def subtract(input: dict):
    
    """
    Tool: subtract
    Description: Takes two integers or floats and returns their difference
    """
    x = input["x"]
    y = input["y"]
    return x - y
